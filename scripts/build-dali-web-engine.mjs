import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const source = resolve(root, "dali-lang/examples/web-eq32-reference.dl");
const compiled = resolve(root, "src/web/generated/dali-web-eq32.generated.js");
const moduleOut = resolve(root, "src/web/generated/daliWebEq32.ts");
const require = createRequire(import.meta.url);
const { parseDali } = require("../dali-lang/src/parser");
const { compileToWebAudioModule } = require("../dali-lang/src/compiler-web-audio");
const { validateSourceLimits, validateProgramSecurity } = require("../dali-lang/src/security-validator");

mkdirSync(dirname(compiled), { recursive: true });

const sourceText = readFileSync(source, "utf8");
validateSourceLimits(sourceText, "web-eq32-reference.dl");
const ast = parseDali(sourceText);
validateProgramSecurity(ast, { mode: "strict" });
const code = compileToWebAudioModule(ast, { securityMode: "strict" });
writeFileSync(compiled, code, "utf8");

writeFileSync(
  moduleOut,
  [
    "/* eslint-disable */",
    "// This file is generated by scripts/build-dali-web-engine.mjs.",
    "// Do not edit by hand; edit dali-lang/examples/web-eq32-reference.dl instead.",
    `export const DALI_WEB_EQ32_SOURCE = ${JSON.stringify(sourceText)};`,
    `export const DALI_WEB_EQ32_ENGINE_JS = ${JSON.stringify(code)};`,
    "",
  ].join("\n"),
  "utf8",
);

console.log(`[dali-web] compiled ${source} -> ${moduleOut}`);
